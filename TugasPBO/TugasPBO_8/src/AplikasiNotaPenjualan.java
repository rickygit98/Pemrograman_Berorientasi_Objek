    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        Float hargaMakanan = Float.valueOf(lblHargaMakanan.getText());
        Float porsiMakanan = Float.valueOf(TxtPorsiMakanan.getText());
        Float hargaMinuman = Float.valueOf(lblHargaMinuman.getText());
        Float porsiMinuman = Float.valueOf(TxtPorsiMinuman.getText());
        lblTotal.setText(String.valueOf(((hargaMakanan)*(porsiMakanan))+((hargaMinuman)*(porsiMinuman))));
    }//GEN-LAST:event_btnHitungActionPerformed

