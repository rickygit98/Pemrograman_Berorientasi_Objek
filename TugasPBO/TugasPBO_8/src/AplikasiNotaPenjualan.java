    private void ComboMakananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboMakananActionPerformed

            String msg = (String)ComboMakanan.getSelectedItem();
            switch(msg){
                case "Nasi Pecel":
                    lblHargaMakanan.setText(HNasiPecel);
                    break;
                case "Nasi Bali Daging":
                    lblHargaMakanan.setText(HNasiBaliDaging);
                    break;
                case "Nasi Campur":
                    lblHargaMakanan.setText(HNasiCampur);
                    break;
                default:
                    lblHargaMakanan.setText("0");
                    break;
                    
            }
                    
        
    }//GEN-LAST:event_ComboMakananActionPerformed

