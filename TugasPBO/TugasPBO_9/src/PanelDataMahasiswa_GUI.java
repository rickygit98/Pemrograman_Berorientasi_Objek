    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        if (list_keyname.getSelectedItem() == "NRP") {
            selectByNrp(in_keyword.getText());
        } else if (list_keyname.getSelectedItem() == "Nama") {
            selectByNama(in_keyword.getText());
        } else if (list_keyname.getSelectedItem() == "Alamat") {
            selectByAlamat(in_keyword.getText());
        } else if (list_keyname.getSelectedItem() == "Jenis Kelamin") {
            String jenis = "";
            if (in_lakilaki.isSelected()) {
                jenis = "L";
            } else if (in_perempuan.isSelected()) {
                jenis = "P";
            } else {
                jenis = in_keyword.getText();
            }
            selectByJK(jenis);
        } else {
            DataTable();
        }
    }//GEN-LAST:event_btn_searchActionPerformed

    private void TabelDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelDataMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) TabelData.getModel(); //create a model
        int selectedRowIndex = TabelData.getSelectedRow(); //get selected row
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //get the primary key to fetch data.
        try {
            Koneksi connect = new Koneksi();
            connect.koneksi();

            String sql = "select * from identitas where nrp like '%" + id + "%'";
            pst = connect.con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            while (rs.next()) {    //u can use normal query
                in_nrp.setText(rs.getString("nrp"));
                in_nama.setText(rs.getString("nama"));
                in_alamat.setText(rs.getString("alamat"));

                String jenis = rs.getString("jenis_kelamin");
                if (jenis.equals("L")) {
                    in_lakilaki.setSelected(true);
                } else {
                    in_perempuan.setSelected(true);
                }
            }
        } catch (Exception ex) {
            //Logger.getLogger(ExistingStock.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TabelDataMouseClicked

