    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here:

        // untuk menentukan nilai dari JenisKelamin berdasarkan check di radiobutton.
        String jenisKelamin = "";
        if (in_lakilaki.isSelected()) {
            jenisKelamin = "L";
        } else if (in_perempuan.isSelected()) {
            jenisKelamin = "P";
        }

        int nrp = Integer.parseInt(in_nrp.getText());
        String nama = in_nama.getText();
        String alamat = in_alamat.getText();

        //panggil method insertnya beserta masukkan parameter yang dibutuhkan
        class_insert insert = new class_insert();
        insert.class_insert(nrp, nama, alamat, jenisKelamin);

        //Update data table nya biar langsung terefresh data baru yang ditambahkan
        DataTable();
    }//GEN-LAST:event_btn_insertActionPerformed

